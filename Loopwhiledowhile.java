public class Loopwhiledowhile {
    public static void main(String[] args) {
        //example of for Loop
        for ( int i = 0; i < 5; i = i + 1) {
            System.out.println("Hello Sumit!");
        }
        //example of while loop
        int i = 0;
        while ( i < 5 ) {
            System.out.println("Hello Dahiya");
            i = i + 1;
        }
        //example of do while 
        int a = 0;
        do {
            System.out.println("Hello family");
            a = a + 1;        
        } while ( a < 5 );
    }
}